"""
Write a function called greet that takes two parameters: name and greeting. Set a default value for greeting as "Hello". The function should print a message like "Hello, <name>!".

- Test the function by calling it with only the name argument and then by calling it with both name and greeting arguments.
"""

def greet(name, greeting="Hello"):
    print(f"{greeting}, {name}!")

greet("Alice", "Hi") 

greet("Bob")  


greet("Alice")

greet("Bob", "Hi")
